class FutureNodeUtil:
    @staticmethod
    def init_future_nodes():
        for k, v in ARRFUTURESNODES.items():
            exchange = ExchangeModel()
            exchange.code = k
            for value in v:
                if isinstance(value, str):
                    exchange.name = value
                if isinstance(value, list):
                    node = StockNode()
                    node.name = value[0]
                    node.code = value[1]
                    node.category = CategoryEnum.FUTURE.name
                    db.session.add(node)
                    db.session.commit()

            exchange.category = CategoryEnum.FUTURE.name
            db.session.add(exchange)
            db.session.commit()

